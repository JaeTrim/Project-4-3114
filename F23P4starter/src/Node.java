public class Node {

    private int index;

    /**
     * Node Constructor
     */
    public Node() {
        index = 0;

    }


    /**
     * Gets the index
     * 
     * @return the index
     */
    public int getIndex() {
        return index;
    }


    /**
     * Sets the index to new index
     * 
     * @param i
     *            is the new index
     */
    public void setIndex(int i) {
        index = i;
    }
}
